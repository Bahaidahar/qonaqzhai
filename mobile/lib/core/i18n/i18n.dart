class LocaleNotifier extends StateNotifier<AppLocale> {
  LocaleNotifier() : super(AppLocale.ru) {
    _restore();
  }

  static const _key = 'qz_locale';
  bool _userOverride = false;

  Future<void> _restore() async {
    final p = await SharedPreferences.getInstance();
    final saved = p.getString(_key);
    // If the user has already picked a locale before _restore resolves, do not
    // clobber their choice. Eliminates a race where the persisted value from a
    // prior session would overwrite an in-flight tap on the onboarding picker.
    if (_userOverride) return;
    if (saved != null) {
      state = AppLocale.values.firstWhere(
        (l) => l.code == saved,
        orElse: () => AppLocale.ru,
      );
    }
  }

  Future<void> set(AppLocale next) async {
    _userOverride = true;
    state = next;
    final p = await SharedPreferences.getInstance();
    await p.setString(_key, next.code);
  }
}

final localeProvider = StateNotifierProvider<LocaleNotifier, AppLocale>((ref) {
  return LocaleNotifier();
});

/// Translate by key for the active locale, falling back to EN, then to the key.
String tr(WidgetRef ref, String key) {
  final locale = ref.watch(localeProvider);
  return _dict[locale]?[key] ?? _dict[AppLocale.en]?[key] ?? key;
}

/// Translate without a Ref — handy in callbacks that already have the locale.
String trFor(AppLocale locale, String key) {
  return _dict[locale]?[key] ?? _dict[AppLocale.en]?[key] ?? key;
}
